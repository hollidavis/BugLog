<template>
  <div class="row mt-3" v-if="account">
    <div class="col-12">
      <div class="bg-light rounded p-3">
        <div class="row">
          <div class=" ml-3 mb-2 mr-auto">
            <h3 class="m-0">
              {{ bug.title }}
            </h3>
          </div>
          <!-- Edit Bug Button -->
          <div v-if="account.id == bug.creatorId">
            <button type="button" class="btn btn-sm btn-success" data-toggle="modal" :data-target="'#editBugModal' + bug.id" v-if="bug.closed === false">
              <span class="fas fa-edit"></span>
            </button>
            <!-- Delete Bug Button -->
            <button type="button" class="btn btn-sm btn-danger mx-2" v-if="bug.closed === false">
              <span class="fa fa-times"></span>
            </button>
          </div>
        </div>
        <!-- Header -->
        <div class="row align-items-center">
          <div class="d-flex align-items-center">
            <!-- Picture -->
            <img class="profile-pic ml-3" :src="bug.creator?.picture" :alt="bug.creator?.name + 'Profile Picture'">
            <!-- Reported/Name -->
            <div class="ml-3">
              <p class="m-0">
                Reported By:
              </p>
              <p class="m-0">
                <b>{{ bug.creator?.name }}</b>
              </p>
            </div>
          </div>
          <!-- Last Updated -->
          <div class="mr-auto">
            <div class="ml-3 text-center">
              <p class="m-0">
                Last Updated:
              </p>
              <p class="m-0">
                <b>{{ state.updatedAt }}</b>
              </p>
            </div>
          </div>
          <!-- Status -->
          <div class="mr-4 text-center">
            <div v-if="bug.closed === false">
              <p class="m-0">
              </p><p class="m-0">
                Status:
              </p>
              <b><p class="m-0">
                ðŸŸ¢ Open
              </p></b>
            </div>
            <div v-else>
              <p class="m-0">
                Status:
              </p>
              <b><p class="m-0">
                ðŸ”´ Closed
              </p></b>
            </div>
          </div>
        </div>
        <div class="row">
          <div class="mx-3 mt-3 text-break">
            <p class="m-0">
              {{ bug.description }}
            </p>
          </div>
        </div>
      </div>
    </div>
  </div>
  <EditBugModal :bug="bug" />
</template>

<script>
import { computed, reactive, watchEffect } from '@vue/runtime-core'
import { AppState } from '../AppState'
export default {
  props: {
    bug: { type: Object, required: true }
  },
  setup(props) {
    const account = computed(() => AppState.account)
    const state = reactive({
      updatedAt: ''
    })
    watchEffect(() => {
      try {
        if (props.bug) {
          const updated = new Date(props.bug.updatedAt)
          state.updatedAt = new Intl.DateTimeFormat('en-US').format(updated)
        }
      } catch (error) {
      }
    })
    return {
      state,
      account
    }
  }
}
</script>

